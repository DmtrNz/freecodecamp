// Запрашиваем у пользователя число N
let N = parseInt(prompt("Введите число N:"));

// Инициализируем переменную для хранения суммы
let sum = 0;

// Используем цикл для перебора всех чисел от 1 до N
for (let i = 1; i <= N; i++) {
    // Проверяем, является ли число четным
    if (i % 2 === 0) {
        sum += i; // Если да, добавляем его к сумме
    }
}

// Выводим результат
console.log(`Сумма всех четных чисел от 1 до ${N} равна ${sum}.`);